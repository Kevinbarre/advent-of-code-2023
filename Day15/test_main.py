from main import part1, part2, parse_steps, hash_algorithm

filename = "example.txt"


def test_part1():
    # Given
    with open(filename) as f:
        lines = f.read().splitlines()
    # When
    result = part1(lines)
    # Then
    assert result == 1320


def test_part2():
    # Given
    with open(filename) as f:
        lines = f.read().splitlines()
    # When
    result = part2(lines)
    # Then
    assert result == 0


def test_parse_steps():
    # Given
    steps = "rn=1,cm-,qp=3,cm=2,qp-,pc=4,ot=9,ab=5,pc-,pc=6,ot=7"
    # When
    result = parse_steps(steps)
    # Then
    assert result == ["rn=1", "cm-", "qp=3", "cm=2", "qp-", "pc=4", "ot=9", "ab=5", "pc-", "pc=6", "ot=7"]


def test_hash_algorithm():
    # Given
    string = "HASH"
    # When
    result = hash_algorithm(string)
    # Then
    assert result == 52
